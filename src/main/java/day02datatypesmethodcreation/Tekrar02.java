package day02datatypesmethodcreation;

public class Tekrar02 {
    public static void main(String[] args) {


        //Ornek 1: Sehir ismi icin bir variable olusturun once "Denizli" sonrada "İstanbul" degeri atayip ekrana yazdirin

        String cityName = "Denizli";
        System.out.println(cityName);

        cityName = "İstabul";
        System.out.println(cityName);

        //Ornek 2: Sehir plaka kodu icin bir variable olusturun ve once "20" sonrada "34" degeri atayip ekrana yazdiriniz

        byte plakaKodu = 20;
        System.out.println(plakaKodu);

        plakaKodu = 34;
        System.out.println(plakaKodu);

        //Ornek 3: Basarili mi sorusu icin bir variable olusturun ve once "false" sonrada "true" degeri atayip yazdirin
         boolean  isSuccessful = false;
        System.out.println(isSuccessful);

        isSuccessful = true;
        System.out.println(isSuccessful);









    }
}
